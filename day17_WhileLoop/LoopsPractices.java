package day17_WhileLoop;

public class LoopsPractices {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("---------------------");

        int num = 1;
        while (num <= 10) {


            System.out.println(num);
            num++;
        }
            System.out.println("---------------------");

            int number = 1;
            do {

                System.out.println(number);
                number++;
            }
            while (number <= 10);
        }
    }

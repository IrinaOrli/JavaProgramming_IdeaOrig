package day18_nestedLoop;

public class Alphbet {
    public static void main(String[] args) {


        for (char j = 'A'; j <= 'E'; j++) {
            for (char i = 'a'; i <= 'z'; i++) {


                System.out.print(j +""+ i+" ");
            }
            System.out.println();
        }
    }
}




